const getFruitHtml = (fruits) => {
    fruits.map(fruit => {
        let html = `
        <div id="${fruit.id}" class="fruit-description-item">
            <h4 class="primary">${fruit.name}</h4>
            ${
                fruit.fruitItems.map(fItem => {
                return `
            <p>${fItem.description}</p>
            <img class="fruit-image image-medium rounded" src="${fItem.image}">
            <a class="d-block my-2" href="./form.html?fruitName=${fruit.name}">Rate it!!</a>           
            `;
                }).join("")
            }
        </div>
        `;
        console.log(html);
        return html;
    });
};

